def clean_text(text: str) -> str:
    """清理文本中的零宽字符和多余空白"""
    text = re.sub(r'[\u200b\u200c\u200d\u200e\u200f\ufeff]', '', text)
    text = re.sub(r'​+', '', text)
    return text.strip()


def get_text(paragraphs, start, end):
    """获取指定范围的段落文本列表"""
    return [clean_text(paragraphs[i].text) for i in range(start, min(end, len(paragraphs))) if clean_text(paragraphs[i].text)]


def parse_ingredients(texts):
    """从文本列表中提取食材"""
    result = []
    full = ' '.join(texts)
    # 按常见分隔符拆分
    parts = re.split(r'[•·；;]', full)
    for part in parts:
        part = part.strip()
        part = re.sub(r'^\d+[\.\、\)）]\s*', '', part)
        part = re.sub(r'^[-–—]\s*', '', part)
        if 5 < len(part) < 200:
            result.append(part)
    return result


def parse_steps(texts):
    """从文本列表中提取步骤"""
    result = []
    for text in texts:
        # 按 • 拆分
        sub = re.split(r'[•·]', text)
        for s in sub:
            s = s.strip()
            s = re.sub(r'^\d+[\.\、\)）]\s*', '', s)
            if len(s) > 10:
                result.append(s)
    return result


def make_recipe(title, category, ingredients, steps, tips=None, difficulty="中等", cooking_time=60, servings="2-4人份"):
    """创建菜谱字典"""
    return {
        "title": title,
        "category": category,
        "subcategory": category,
        "tags": [category, "甜品" if category == "烘焙" else "饮料"],
        "difficulty": difficulty,
        "cookingTime": cooking_time,
        "servings": servings,
        "ingredients": ingredients,
        "steps": steps,
        "tips": tips or []
    }


